/// Returns the division (juz/hizb/quarter) that contains the given page.
QuranDivision divisionForPage(int page) {
  var result = kQalounDivisions.first;
  for (final d in kQalounDivisions) {
    if (d.startPage <= page) {
      result = d;
    } else {
      break;
    }
  }
  return result;
}

/// Returns the 8 quarter divisions for a given juz (1-30).
List<QuranDivision> divisionsForJuz(int juz) {
  return kQalounDivisions.where((d) => d.juz == juz).toList();
}
